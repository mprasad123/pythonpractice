def printMonth(dn):
    if(dn==1):
        return "january"
    elif(dn==2):
        return "feburary"
    elif(dn==3):
        return "march"
    elif(dn==4):
        return "april"
    elif(dn==5):
        return "may"
    elif(dn==6):
        return "june"
    elif(dn==7):
        return "july"
    else:
        return "invalid"
